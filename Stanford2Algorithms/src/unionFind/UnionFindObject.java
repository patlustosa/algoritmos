package unionFind;

public class UnionFindObject<T> {

	private T object;
	private T parent;
	
	public T getObject() {
		return object;
	}
	public void setObject(T object) {
		this.object = object;
	}
	public T getParent() {
		return parent;
	}
	public void setParent(T parent) {
		this.parent = parent;
	}
	
	
	
}
